                                        <p>A kind gesture appreciated by all your guests is a short speech by you and your partner addressing everyone present at the wedding. Your guests all came for you and they want to hear from you.  And let the major players know much you appreciate them for helping you with your best day ever. Ensure that you don’t forget anyone by making a list of all the people you want to thank in advance and get someone else to check it! Bring a few copies of the speech with you and hand one to a responsible guest just in case your own copy goes missing.
                                        </p>
		<p><h4 class="bold-text">9. Foregoing the Videographer </h4> </p>
                                        <!--<blockquote>
                                            <p> Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus.   </p>

                                        <p>This is an easy wedding planning mistake to make for newly engaged couples. To-be-weds will leave the task of securing hotel room blocks for out-of-town wedding guests until the last minute. If you're marrying during a busy time and you don't look into hotel availability in advance, you can end up with no rooms for your guests so reserve as early as possible. Begin your research up to a year in advance, and make sure your block is booked at least by the eight-month mark—if not sooner. Include hotel information in your save-the-date cards, wedding website and invitations. (FYI, you're just setting them aside—your guests will put down their own credit cards when they call to book the rooms.)
                                        </p>


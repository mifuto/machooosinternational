                                        <p>This one comes down to making sure your DJ or whomever your having set up the sound system has professional wedding experience. Asking them perform sound checks before guests arrive is always a good plan.  And possibly asking for a microphone 101 tutorial during one of your consultations can be helpful. We’ve all been to those weddings where the sound system has really bad feedback, cuts in and out or just doesn’t work at all. There’s nothing more awkward than having 100 guests stare at you and sit in silence while they can’t hear what your officiant says or your vows to each other.  
                                        </p>
				<p><h4 class="bold-text">7. Forgetting your Vows </h4> </p>
                                        <!--<blockquote>
                                            <p> Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus.   </p>

                                        <p>You would think that there is no one else you would spend time with on your wedding day then partner. Sadly, this isn't true. With all the hair and makeup, socializing with guests and photos with bridal parties, couples actually spend less than 40% of the day together.  And that  percentage is even lower when couples decide to forego the first look. When there is no first look, couples won't actually see each other until the ceremony which is typically in the later afternoon and more than half of the day is over. 
Pro Tip: Doing a first look allows you and your partner to see each much earlier in the day and spend more time together on your wedding day. If you decide to forego the first look, think about stealing a little time together immediately after the ceremony or going for a short walk together while your guests grab a drink right after the speeches. A few minutes together to reconnect with each other will make all the difference.

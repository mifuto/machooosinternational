                                        <h4 class="bold-text">Don’t let these common wedding day mistakes ruin your special day! </h4>
                                        <p>When you’re planning your wedding, you want the big day to be absolutely perfect. The teams at our amazing venues will always do everything they can (and a little bit more) to give you the best day possible but there a still a few wedding day mistakes that too many couples make. The good news is that they’re all really easy to avoid and with these wedding planning tips from Machooos International, wedding mistakes will be a thing of the past! </p>
                                        <p><h4 class="bold-text">1. Running out of time as you’re getting ready
 </h4> </p>

                                        <p>Save yourself from any anxiety or terrifying feelings by writing down/typing up your personal vows on small note cards or vow books. It’s easy to get caught up in the emotion of the ceremony and going blank when the pressure is on. Have your planner give them to the officiant in advance with clear direction on when (and if) he or she should pass along the cards to you and your partner. Remember, even the most seasoned public speakers can often be swept away by the emotion of the moment, leaving you speechless and searching for those vows you swore you had memorized. Even if you and your fiancé are the most cool, calm, and collected couple ever—don’t skip this step. After all, it’s best to have the cards and not need them, than to not have them and need them.
                                        </p><p><h4 class="bold-text">8. Forgetting to say “Thank You!” </h4> </p>
                                        <!--<blockquote>
                                            <p> Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus.   </p>

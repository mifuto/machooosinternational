                                        <p>Oh yes, this happens far too often. One minute, you’re lingering in the shower, the next you’re panicking because it’s time to leave. “Speak to your hair and makeup team about how much time they need and ask them to give you a detailed timeline of their plans. That way, if you feel the timings are tight, you can talk it through in advance of the day. Often hair and makeup artists factor in additional time but it’s always worth knowing that beforehand.”
                                        </p>
									<p><h4 class="bold-text">2. Feeling a bit shaky </h4> </p>
                                        <!--<blockquote>

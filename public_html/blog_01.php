                                        <p>We tell all our brides how important it is to eat a meal that can sustain them on their wedding day. It doesn’t need to be fancy – some sandwiches or even just little snacks will make sure that you eat before the main event and ensure your energy and blood sugar levels stay appropriate. Not eating combined with the nerves and anticipation of the wedding day can lead to a bride or groom feeling shaky and lightheaded right before walking down the aisle. 
                                        </p>
								<p><h4 class="bold-text">3. Still organizing and planning on the big day </h4> </p>
                                        <!--<blockquote>
                                            <p> Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus.   </p>

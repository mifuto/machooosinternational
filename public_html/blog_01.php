                                        <p>Photos are a must for most couples, but they only take you so far—videos let you hear your and your partner's voices as you say your vows and watch your friends tear up the dance floor. By hiring a professional videographer to document your wedding and craft your wedding film, you will find yourself immersed in the love story of your wedding day in a way that surpasses transcends photography and that only a wedding video could do for you. 
                                        </p>
	<p><h4 class="bold-text">10.  (Free Bonus)  Booking Guest Hotel Rooms Blocks Too Late </h4> </p>
                                        <!--<blockquote>
                                            <p> Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus. Donec auctor et urnaLorem ipsum dolor sit amet, consectetur adipiscing elit. Cras lacinia magna vel molestie faucibus.Cras lacinia magna vel molestie faucibus.   </p>
